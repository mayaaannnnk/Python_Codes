# Arithmetic Operators 

#plus+ operator
print(2+5)              #7

#minus- operator
print(5-2)              #3

#multiply* operator
print(2*2)              #4

#divide/ operator
print(80/10)            #8.0

#divide// operator but result only in integer
print(5/2)              #2.5
print(5//2)             #2

#remainder% operator
print(5%2)              #1

#power** operator
print(2**2)            #4