#type conversion to float(), string str(), boolean bool()

a=input("enter first number:")
b=input("enter second number:")

c=a+b #taking number as a string "a"+"b"
print("without type conversion ",c ) #ab

#type conversion to integer
sum=int(a)+int(b) #a+b=sum
print("with type conversion to integer",sum)#sum

#type  conversion to float
sum=float(a)+float(b) #a+b=sum
print("with type conversion to float",sum)#sum

#type conversion to string
sum=str(a)+str(b) #"a"+"b"
print("with type conversion to string",sum)#sum=ab

#type conversion to boolean
d=1
print("with type conversion to boolean ",bool(d))


